package date.willnot.amy.rts.api.unit;

/**
 * The type of a unit. Units are split into two types - ranged units and
 * non-ranged units. Ranged units are able to attack from a distance, while
 * non-ranged units are only able to attack immediately adjacent enemies.
 *
 * @author amy
 * @since 5/1/16.
 */
public enum UnitType {
    RANGED, NOT_RANGED
}
